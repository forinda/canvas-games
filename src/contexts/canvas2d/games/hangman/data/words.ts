export function getRandomWord(): WordEntry {
	const cat = ALL_CATEGORIES[Math.floor(Math.random() * ALL_CATEGORIES.length)];
	const word = cat.words[Math.floor(Math.random() * cat.words.length)];

	return { word, category: cat.name };
}
